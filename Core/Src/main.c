int get_key_val()
{
	if(HAL_GPIO_ReadPin(GPIOB, GPIO_PIN_0)==0)
	{
		return 2;
	}
	if(HAL_GPIO_ReadPin(GPIOB, GPIO_PIN_1)==0)
	{
		return 1;
	}
	return 0;
}
